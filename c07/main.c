#include <stdio.h>

void main()
{
    //!this is variable example
    int varNum = 5;
    printf("varNum = %d\n", varNum);
    //you can resigne variable value
    varNum = 10;
    printf("varNum = %d\n", varNum);
    //!this is constant example
    const int CONST_NUM  = 5;
    printf("CONST_NUM = %d\n", CONST_NUM);
    
}